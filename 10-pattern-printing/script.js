// ===============================
// Pattern 1
// *
// **
// ***
// ****
// *****
// ===============================

for (let i = 1; i <= 5; i++) {

    let star = "";

    for (let j = 1; j <= i; j++) {
        star += "*";
    }

    console.log(star);
}

// ===============================
// Pattern 2
// *****
// ****
// ***
// **
// *
// ===============================

for (let i = 5; i >= 1; i--) {

    let star = "";

    for (let j = i; j >= 1; j--) {
        star += "*";
    }

    console.log(star);
}